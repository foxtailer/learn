print(f"{1.4938539:.2f}")  # 1.49
print(f"{1.4938539:.0f}")  # 1
print(f"{1000000.4938539:_.3f}")  # 1_000_000.494
print(f"{1000000:,}")  # 1,000,000
a = 5
b = 6
print(f"{a+b = }")  # a+b = 11
print(f"{bool(a) = }")  # bool(a) = True
print(f"{a:03}")  # 005

print(f"{a:>20}")   #                    5
print(f"{a:<20}:")  # 5                   :
print(f"{a:^20}:")  #          5          :
print(f"{a:*^20}")  #*********5**********
